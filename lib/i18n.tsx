export type Translations = typeof translations.ru

// ─── Context ──────────────────────────────────────────────────────────────────

const I18nContext = createContext<{ t: Translations; language: Language } | null>(null)

export function I18nProvider({
  children,
  language,
}: {
  children: React.ReactNode
  language: Language
}) {
  const t = translations[language] as unknown as Translations
  return <I18nContext.Provider value={{ t, language }}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used within I18nProvider')
  return ctx
}
